package chapter03;

public class Operator10 {

	public static void main(String[] args) {
		
		String str1 = "JAVA";
		String str2 = "Programing";
		
		// str1, str2 = 참조변수 -> 객체 가르키는 메모리 주소값 저장
		// 참조변수의 관계연산은 ==,!= 두 개만 이루어짐
		
		System.out.println(str1 == str2);
		System.out.println(str1 != str2);
		
		// System.out.println(str1 > str2); //오류
				
		
	}

}
